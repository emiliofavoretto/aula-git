let n1 = 10
let n2 = 2
let conta = 'multiplicação'

switch(conta){
    case 'adição':
        console.log(n1 + n2)
        break
    case 'subtração':
        console.log(n1 - n2)
        break
    case 'multiplicação':
        console.log(n1 * n2)
        break
    case 'divisão':
        console.log(n1 / n2)
        break
}